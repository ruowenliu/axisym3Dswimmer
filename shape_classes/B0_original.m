function val = B0_original(t)
% fifth order B-spline function, no shift, simplified version
p1 = t.^5/120;
p2 = (-5*t.^5+30*t.^4-60*t.^3+60*t.^2-30*t+6)/120;
p3 = (5*t.^5-60*t.^4+270*t.^3-570*t.^2+585*t-237)/60;
p4 = (5*(6-t).^5-60*(6-t).^4+270*(6-t).^3-570*(6-t).^2+585*(6-t)-237)/60;
p5 = (-5*(6-t).^5+30*(6-t).^4-60*(6-t).^3+60*(6-t).^2-30*(6-t)+6)/120;
p6 = ((6-t).^5)/120;
val = p1.*(t>=0 & t<1)+p2.*(t>=1 & t<2)+p3.*(t>=2 & t<3)+p4.*(t>=3 & t<4)+p5.*(t>=4 & t<5)+p6.*(t>=5 & t<6);
end